package homework.day2.pretask;

public class MathOperatorsExample {
    public static void main(String[] args) {
        int a = 30;
        int b = 1249;
        int sum = a + b;
        System.out.print(sum + ", ");
        int substr = a - b;
        System.out.print(substr + ", ");
        int mult = a * b;
        System.out.print(mult + ", ");
        int div = a / b;
        System.out.print(div);

    }
}
